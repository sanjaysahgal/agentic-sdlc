// Block M1 of the approved system-wide plan
// (~/.claude/plans/rate-this-plan-zesty-tiger.md). New-agent scaffold
// script. Generates the skeleton files a new agent needs and prints the
// remaining manual steps from docs/CONTRIBUTING_AGENTS.md so the author
// has a checklist to drive against.
//
// Usage:
//   npx tsx scripts/scaffold-agent.ts <agent-name> [--dry-run]
//
// Example:
//   npx tsx scripts/scaffold-agent.ts coder
//
// Generates:
//   agents/<name>.ts                                 — skeleton prompt builder + tools
//   tests/unit/<name>-prompt.test.ts                 — smoke test for the prompt builder
//   tests/fixtures/agent-output/<name>/.gitkeep      — placeholder for real-fixture tests
//
// Then prints the remaining manual steps from CONTRIBUTING_AGENTS.md.

import * as fs from "node:fs"
import * as path from "node:path"

interface CliArgs {
  name:    string
  dryRun:  boolean
}

function parseArgs(argv: string[]): CliArgs {
  const args: Partial<CliArgs> = { dryRun: false }
  for (const a of argv) {
    if (a === "--dry-run") { args.dryRun = true; continue }
    if (a.startsWith("--")) continue
    if (!args.name) args.name = a
  }
  if (!args.name) {
    throw new Error("Usage: npx tsx scripts/scaffold-agent.ts <agent-name> [--dry-run]")
  }
  if (!/^[a-z][a-z0-9-]*$/.test(args.name)) {
    throw new Error(`Invalid agent name: "${args.name}". Use lowercase kebab-case (e.g. "coder", "frontend").`)
  }
  return args as CliArgs
}

function pascalCase(name: string): string {
  return name.split("-").map((part) => part.charAt(0).toUpperCase() + part.slice(1)).join("")
}

function upperSnake(name: string): string {
  return name.toUpperCase().replace(/-/g, "_")
}

const REPO_ROOT = path.join(__dirname, "..")

function pathFor(name: string): {
  agentFile:    string
  testFile:     string
  fixtureDir:   string
  fixtureGitkeep: string
} {
  return {
    agentFile:      path.join(REPO_ROOT, `agents/${name}.ts`),
    testFile:       path.join(REPO_ROOT, `tests/unit/${name}-prompt.test.ts`),
    fixtureDir:     path.join(REPO_ROOT, `tests/fixtures/agent-output/${name}`),
    fixtureGitkeep: path.join(REPO_ROOT, `tests/fixtures/agent-output/${name}/.gitkeep`),
  }
}

