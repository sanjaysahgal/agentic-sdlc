function writeIfMissing(filePath: string, content: string, dryRun: boolean): "wrote" | "exists" | "would-write" {
  if (fs.existsSync(filePath)) return "exists"
  if (dryRun) return "would-write"
  fs.mkdirSync(path.dirname(filePath), { recursive: true })
  fs.writeFileSync(filePath, content)
  return "wrote"
}

